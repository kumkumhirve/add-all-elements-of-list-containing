'''Q3: WAP to add all elements of list containing first 20 natural
number?'''

# Create a list of the first 20 natural numbers (1 to 20)
natural_numbers = list(range(1, 21))

# Calculate the sum of all elements in the list
total = sum(natural_numbers)

# Display the result
print("The list of first 20 natural numbers is:")
print(natural_numbers)

print("\nThe sum of all elements is:", total)
